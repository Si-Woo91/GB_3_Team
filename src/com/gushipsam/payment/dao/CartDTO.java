package com.gushipsam.payment.dao;

public class CartDTO {
	private int cID;
	private int gID;
	private int cQTY;
	private String gNAME;
	private String gBRAND;
	private int gPrice;
	
	public int getcID() {
		return cID;
	}
	public void setcID(int cID) {
		this.cID = cID;
	}
	public int getgID() {
		return gID;
	}
	public void setgID(int gID) {
		this.gID = gID;
	}
	public int getcQTY() {
		return cQTY;
	}
	public void setcQTY(int cQTY) {
		this.cQTY = cQTY;
	}
	public String getgNAME() {
		return gNAME;
	}
	public void setgNAME(String gNAME) {
		this.gNAME = gNAME;
	}
	public String getgBRAND() {
		return gBRAND;
	}
	public void setgBRAND(String gBRAND) {
		this.gBRAND = gBRAND;
	}
	public int getgPrice() {
		return gPrice;
	}
	public void setgPrice(int gPrice) {
		this.gPrice = gPrice;
	}
	
	
	
	
	
	
	
}
    

    
